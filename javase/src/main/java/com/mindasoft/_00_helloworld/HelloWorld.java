package com.mindasoft._00_helloworld;

/**
 * 这里是类的注释，用来是该类是来用干嘛的！
 * Created by huangmin on 2017/9/15 16:37.
 */
public class HelloWorld {
    // 1、public class 用来标识HelloWorld这里来，说明HelloWorld是一个类

    public static void main(String[] args) {
        // 2、public static void main 是java程序的总入口，args 是启动时，外部传入的参数。
        System.out.println("Hello World!");
        // 3、打印Hello World! 程序的控制台
    }
}
